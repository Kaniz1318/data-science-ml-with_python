#Exercise 1.2: Calculate Sum of List
#Task: Given a list of numbers, calculate their sum using a for loop.

#numbers = [12, 35, 9, 56, 24]
#Expected Output:
#The sum of [12, 35, 9, 56, 24] is: 136
numbers = [12, 35, 9, 56, 24]
sum=0

for i in numbers:
    sum=sum+i
print(f"The sum of {numbers} is: {sum}")
   
    
    
    